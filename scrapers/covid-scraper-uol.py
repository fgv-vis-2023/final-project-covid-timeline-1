    url = page_url
    page = urlopen(url)
    html = page.read().decode("utf-8")
    soup = BeautifulSoup(html, "html.parser")

    thumb_text = soup.find_all("h3", class_="thumb-title")
    thumb_text = [thumb_text[i].text for i in range(len(thumb_text))]

    thumb_layer = soup.find_all("div", class_="thumb-layer")
    thumb_img = [thumb_layer[i].find("img").attrs['data-src'] for i in range(len(thumb_layer))]

    thumb_wrapper = soup.find_all("div", class_="thumbnails-wrapper")
    news_urls = [thumb_wrapper[i].find("a").attrs['href'] for i in range(len(thumb_wrapper))]
    
    date = soup.find_all("time", class_="thumb-date")
    date = [pd.to_datetime(date[i].text).strftime('%Y-%m-%d') for i in range(len(date))]

    number_of_elements = len(thumb_text)
    
    for element in range(number_of_elements):
        date_str = date[element]
        content_str = thumb_text[element]
        media_url = thumb_img[element]
        news_url = news_urls[element]
        if date_str != '' and content_str != '' and media_url != '':
            if date_str not in pages_dict:
                pages_dict[date_str] = []
            pages_dict[date_str].append({'content': content_str, 'media': media_url, 'news_url': news_url ,'source': 'UN'})
